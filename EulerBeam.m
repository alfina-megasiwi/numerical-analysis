function [A,b] = EulerBeam(n)
    A = zeros(n,n);
    A(1,1:4) = [16 -9 8/3 -1/4];
    A(2,1:4) = [-4 6 -4 1];
    for i=3:n-2
        A(i,i-2:i+2)=[1 -4 6 -4 1];
    endfor
    A(n-1,n-3:n) = [16/17 -60/17 72/17 -28/17];
    A(n,n-3:n) =  [-12/17 96/17 -156/17 72/17];
    
    w = 0.3; d = 0.03; g = 9.81; E = 1.3*10^10;
    h = 2/n; I = (w*(d^3))/12;
    f = -480*w*d*g;
    b = (h^4/(E*I)) * f * ones(n,1);
endfunction

